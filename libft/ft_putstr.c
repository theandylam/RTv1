#include "libft.h"

int	ft_putstr(char const *s)
{
	int i;

	if (s == NULL)
		return (0);
	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar(s[i]);
		i++;
	}
	return (i);
}
