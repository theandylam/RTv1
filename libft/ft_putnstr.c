#include "libft.h"

int	ft_putnstr(char const *s, int n)
{
	int i;

	if (s == NULL || n < 0)
		return (-1);
	i = 0;
	while (s[i] != '\0' && i < n)
	{
		ft_putchar(s[i]);
		i++;
	}
	return (i);
}
